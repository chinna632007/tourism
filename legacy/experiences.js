// Helper: Create experience card HTML
function createExperienceCard(exp) {
    return '<div class="experience-card fade-in">' +
        '<img src="' + exp.image + '" alt="' + exp.title + '" loading="lazy">' +
        '<div class="experience-card-content">' +
        '<h3>' + exp.title + '</h3>' +
        '<p>' + exp.description + '</p>' +
        '</div></div>';
}

// Helper: Create subcategory card HTML
function createSubcategoryCard(item) {
    return '<div class="subcategory-card fade-in">' +
        '<img src="' + item.image + '" alt="' + item.title + '" loading="lazy">' +
        '<div class="subcategory-card-content">' +
        '<h4>' + item.title + '</h4>' +
        '<p>' + item.description + '</p>' +
        '</div></div>';
}

// Tab switching functionality
function switchTab(category) {
    // Update active tab button
    var tabs = document.querySelectorAll('.nav-tab');
    for (var i = 0; i < tabs.length; i++) {
        if (tabs[i].getAttribute('data-category') === category) {
            tabs[i].classList.add('active');
        } else {
            tabs[i].classList.remove('active');
        }
    }

    // Update active section
    var sections = document.querySelectorAll('.category-section');
    for (var i = 0; i < sections.length; i++) {
        if (sections[i].id === category) {
            sections[i].classList.add('active');
        } else {
            sections[i].classList.remove('active');
        }
    }

    // Scroll to categories nav
    document.querySelector('.categories-nav').scrollIntoView({ behavior: 'smooth', block: 'start' });
}

// Populate all sections
document.addEventListener('DOMContentLoaded', function () {
    // Eco tourism
    var ecoGrid = document.getElementById('eco-tourism-grid');
    if (ecoGrid) {
        var ecoHtml = '';
        for (var i = 0; i < ecoTourismData.length; i++) {
            ecoHtml += createSubcategoryCard(ecoTourismData[i]);
        }
        ecoGrid.innerHTML = ecoHtml;
    }

    // Artforms
    var artGrid = document.getElementById('artforms-grid');
    if (artGrid) {
        var artHtml = '';
        for (var i = 0; i < artformsData.length; i++) {
            artHtml += createSubcategoryCard(artformsData[i]);
        }
        artGrid.innerHTML = artHtml;
    }

    // Cuisine
    var cuisineGrid = document.getElementById('cuisine-grid');
    if (cuisineGrid) {
        var cuisineHtml = '';
        for (var i = 0; i < cuisineData.length; i++) {
            cuisineHtml += createSubcategoryCard(cuisineData[i]);
        }
        cuisineGrid.innerHTML = cuisineHtml;
    }

    // RT Packages
    var rtGrid = document.getElementById('rt-packages-grid');
    if (rtGrid) {
        var rtHtml = '';
        for (var i = 0; i < rtPackagesData.length; i++) {
            rtHtml += createSubcategoryCard(rtPackagesData[i]);
        }
        rtGrid.innerHTML = rtHtml;
    }

    // Tab click handlers
    var tabButtons = document.querySelectorAll('.nav-tab');
    for (var i = 0; i < tabButtons.length; i++) {
        tabButtons[i].addEventListener('click', function () {
            var category = this.getAttribute('data-category');
            switchTab(category);
        });
    }

    // Scroll Animation Observer
    var observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    };

    var observer = new IntersectionObserver(function (entries) {
        entries.forEach(function (entry) {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
            }
        });
    }, observerOptions);

    var fadeElements = document.querySelectorAll('.fade-in');
    for (var i = 0; i < fadeElements.length; i++) {
        observer.observe(fadeElements[i]);
    }

    // Header scroll effect
    window.addEventListener('scroll', function () {
        var header = document.querySelector('header');
        var currentScroll = window.pageYOffset;

        if (currentScroll > 100) {
            header.style.background = 'rgba(26, 32, 44, 0.95)';
            header.style.backdropFilter = 'blur(10px)';
        } else {
            header.style.background = 'linear-gradient(to top, transparent 1%, rgba(0,0,0,0.1) 100%)';
            header.style.backdropFilter = 'none';
        }
    });
});